# OPERATOR OVERLOADING

"""
Operator Overloading is a mechanism that
allows us to give new meaning to twist existing operators
"""

# Python Program to show use of
print(3 + 5)
# + Operator for different purpose
print('project' + ' ' + 'maker')
# Concatenate two strings
sum = 'project' + ' ' + 'maker'
print(sum)
# Product two numbers
print(3 * 4)
# Repeat the String
print('project ' * 5)


